function fresnelToCoeffs(eta, kappa) {
    var coeffs = [];
    coeffs[0] = fresnel(1.0, eta, kappa);
    coeffs[1] = (coeffs[0] >= 0.0) ? 1.0 - coeffs[0] : 0.0;

    const ct1 = 0.12812812812812813;
    const ct2 = 0.43243243243243246;
    const i1  = 128;
    const i2  = 432;

    const dF1 = fresnel(ct1, eta, kappa) - (coeffs[0] + coeffs[1]*fresnel_curves[i1][1]);
    const dF2 = fresnel(ct2, eta, kappa) - (coeffs[0] + coeffs[1]*fresnel_curves[i2][1]);
    
    const A11 =  fresnel_curves[i2][3];
    const A12 = -fresnel_curves[i2][2];
    const A21 = -fresnel_curves[i1][3];
    const A22 =  fresnel_curves[i1][2];
    const det = A11*A22 - A12*A21;
    
    coeffs[2] = (A11*dF1 + A21*dF2) / det;
    coeffs[3] = (A12*dF1 + A22*dF2) / det;
    return coeffs;
}

function evaluateFresnelCurves(cT) {
    var index = Math.trunc(cT*NB_POINTS);
    index = Math.min(index, NB_POINTS-1);
    var values = [];
    values[0] = fresnel_curves[index][0];
    values[1] = fresnel_curves[index][1];
    values[2] = fresnel_curves[index][2];
    values[3] = fresnel_curves[index][3];
    return values;
}

function eval_basis(basis_k, N=100) {
    var data = { x: [], y: []};
    for(var k=0; k<N; ++k) {
        var x = 0.5*Math.PI * k/(N-1);
        var y = evaluateFresnelCurves(Math.cos(x))[basis_k];
        data.x[k] = x;
        data.y[k] = y;
    }
	data.name = 'b_' + basis_k + ''
	data.line = {};
	if(basis_k == 0) {
		data.line.width = 2;
	}

	const colors = ["blue", "orange", "green", "red"];
	data.line.color = colors[basis_k];
    return data;
}

function eval_decomposition_nk(eta, kappa, N=100) {
	var coeffs = fresnelToCoeffs(eta, kappa);

    var data = { x: [], y: []};
    for(var k=0; k<N; ++k) {
        var x = 0.5*Math.PI * k/(N-1);
		var y = 0.0;
		for(var basis_k=0; basis_k<4; ++basis_k)
			y += coeffs[basis_k] * evaluateFresnelCurves(Math.cos(x))[basis_k];
        data.x[k] = x;
        data.y[k] = y;
    }
    data.name = 'Ours'
    return data;
}

