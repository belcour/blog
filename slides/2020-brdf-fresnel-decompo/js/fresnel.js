function RGtoNK2(r, g) {
    if(r <= 0.0) {
        return [1.0, 0.0];
    }
    var n = g*(1.0-r)/(1.0+r) + (1.0-g)*(1.0+Math.sqrt(r))/(1.0-Math.sqrt(r));
    var k2 = (g==0) ? 0.0 : (r*(1.0+n)*(1.0+n) - (n-1.0)*(n-1.0)) / (1.0-r);
    return [n, k2];
}

function RGtoNK(r, g) {
    var nk2 = RGtoNK2(r, g);
    return [nk2[0], Math.sqrt(nk2[1])];
}

