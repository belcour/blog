function gulbrandsen_coeffs(K=100) {
	var data_Ccond = { x: [], y: [], marker: { color: 'black' }};
	var data_Cdiel = { x: [], y: [], marker: { color: 'red' }};
	var data_scatter = { x: [], y: [], marker: { color: [], size: 2, colorscale: 'Viridis'}};
    for(var k=0; k<K; ++k) {
		const a  = k/(K-1)
		const r  = a*1.0 + (1.0-a)*1.0E-2;

		// Cdiel curve
		{
			const nk = RGtoNK(r, 0.0);
			const c  = fresnelToCoeffs(nk[0], nk[1]);
			data_Cdiel.x[k] = c[0];
			data_Cdiel.y[k] = c[2];
		}
		
		// Ccond curve
		{
			const nk = RGtoNK(r, 1.0);
			const c  = fresnelToCoeffs(nk[0], nk[1]);
			data_Ccond.x[k] = c[0];
			data_Ccond.y[k] = c[2];
		}
	}
	
	for( var i=0; i<50; ++i) {
		for( var j=0; j<50; ++j) {
			const a  = i/49
			const r  = a*1.0 + (1.0-a)*1.0E-2;
			const g  = j/49;

			const nk = RGtoNK(r, g);
			const c  = fresnelToCoeffs(nk[0], nk[1]);
			data_scatter.x[i*50+j] = c[0];
			data_scatter.y[i*50+j] = c[2];
			data_scatter.marker.color[i*50+j] = (i*50+j)/(50*50);
		}
	}
	data_scatter.mode = 'markers'
	data_scatter.type = 'scatter'

    return [data_scatter, data_Cdiel, data_Ccond];
}

