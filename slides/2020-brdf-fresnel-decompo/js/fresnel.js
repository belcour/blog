function polarized_fresnel(CosTheta, Eta) {
    // Code from Seb. Lagarde's blog :
    // Compute Fresnel formula for a given direction and ior
    // ior : eta_t / eta_i = 1.0 / eta_coat
    var SinTheta2 = 1 - CosTheta * CosTheta;
    var temp = 1 - (SinTheta2 / (Eta * Eta));
    var tempo = (temp < 0) ? 1.0 : temp;
    var t0 = Math.sqrt(tempo);
    var t1 = Eta * t0;
    var t2 = Eta * CosTheta;
    var rs = (CosTheta - t1) / (CosTheta + t1);
    var rp = (t0 - t2) / (t0 + t2);
    return 0.5*(rs*rs + rp*rp);
}

function fresnel_dielectric_conductor(Eta, Etak, CosTheta) {
    var CosTheta2 = CosTheta * CosTheta;
    var SinTheta2 = 1 - CosTheta2;
    var Eta2 = Eta * Eta;
    var Etak2 = Etak * Etak;

    var t0 = Eta2 - Etak2 - SinTheta2;
    var a2plusb2 = Math.sqrt(t0 * t0 + 4 * Eta2 * Etak2);
    var t1 = a2plusb2 + CosTheta2;
    var temp = 0.5 * (a2plusb2 + t0);
    temp = (temp > 0.0) ? temp : 1.0;
    
    var a = Math.sqrt(temp);
    var t2 = 2 * a * CosTheta;
    var Rs = (t1 - t2) / (t1 + t2);

    var t3 = CosTheta2 * a2plusb2 + SinTheta2 * SinTheta2;
    var t4 = t2 * SinTheta2;   
    var Rp = Rs * (t3 - t4) / (t3 + t4);
    return 0.5 * (Rs + Rp);
}

function fresnelConductorExact(cosThetaI, eta, k) {
    /* Modified from "Optics" by K.D. Moeller, University Science Books, 1988 */

    var cosThetaI2 = cosThetaI*cosThetaI,
          sinThetaI2 = 1-cosThetaI2,
          sinThetaI4 = sinThetaI2*sinThetaI2;

    var temp1 = eta*eta - k*k - sinThetaI2,
        a2pb2 = Math.sqrt(temp1*temp1 + 4*k*k*eta*eta),
        a     = Math.sqrt(0.5 * (a2pb2 + temp1));

    var term1 = a2pb2 + cosThetaI2,
        term2 = 2*a*cosThetaI;

    var Rs2 = (term1 - term2) / (term1 + term2);

    var term3 = a2pb2*cosThetaI2 + sinThetaI4,
        term4 = term2*sinThetaI2;

    var Rp2 = Rs2 * (term3 - term4) / (term3 + term4);

    return 0.5 * (Rp2 + Rs2);
}

function schlick_approx(theta, eta0, eta1) {
    R0 = R_0(eta0, eta1);
    return R0 + (1- R0)*Math.pow(1 - Math.cos(theta), 5);
}


